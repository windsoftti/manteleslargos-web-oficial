<?php

/*
|--------------------------------------------------------------------------
| Detectar ambiente
|--------------------------------------------------------------------------
|
| local
| development
| production
|
*/

$host = strtolower($_SERVER['HTTP_HOST'] ?? '');

switch ($host) {

    /*
    |--------------------------------------------------------------------------
    | Localhost
    |--------------------------------------------------------------------------
    */

    case 'localhost':

        define(
            'APP_ENV',
            'local'
        );

        break;

    /*
    |--------------------------------------------------------------------------
    | Desarrollo
    |--------------------------------------------------------------------------
    */

    case 'dev.manteleslargos.com':

        define(
            'APP_ENV',
            'development'
        );

        break;

    /*
    |--------------------------------------------------------------------------
    | Producción
    |--------------------------------------------------------------------------
    */

    default:

        define(
            'APP_ENV',
            'production'
        );

}